// while loop

let index = 0
while (index <= 10) // here we give the condition and according to that condition loop will execute.
{
    // console.log(`Value of index is ${index}`); // here we simply print the value of index. 
    index += 2 // here we increment the index value by 2.
}

let myArray = ['flash', "batman", "superman"] // here we create an array.

let arr = 0

while (arr < myArray.length) 
{
    // console.log(`Value is ${myArray[arr]}`); here we simply print the array.
    arr += 1 // here we increment the array by 1.
}

